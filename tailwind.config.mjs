/**
 * @param {string} propertyName
 */
const getProgressAnimation = (propertyName) => {
  return Array.from({ length: 101 }, (_, i) => ({
    [`${i}%`]: { [propertyName]: `${i}` },
  })).reduce((acc, cur) => ({ ...acc, ...cur }), {});
};

